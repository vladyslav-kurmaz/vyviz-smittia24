export function getServicePageContent(service: Service): ServicePageContent {
  const galleryId = SERVICE_GALLERY_ID[service.slug] ?? 1;
  const heroImages = seoHeroImages(service.seoPath, galleryId);
  const page = PAGES[service.slug];

  if (page) {
    return {
      heroTitle: `${service.name} в Києві та області`,
      heroImages,
      ...page,
    };
  }

  return {
    heroTitle: `${service.name} в Києві та області`,
    heroSubtitle: service.description,
    heroImages,
    paragraphs: [service.description],
    bullets: [],
    faq: defaultFaq(service.name),
  };
}
